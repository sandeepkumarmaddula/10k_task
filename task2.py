#All Bitwise Operators
a = 5    # 0101
b = 3    # 0011
print("a =", a)
print("b =", b)
# AND
print("a & b =", a & b)
# OR
print("a | b =", a | b)
# XOR
print("a ^ b =", a ^ b)
# NOT
print("~a =", ~a)
# Left Shift
print("a << 1 =", a << 1)
# Right Shift
print("a >> 1 =", a >> 1)